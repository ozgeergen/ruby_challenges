weather = "cloudy"
case weather
when "sunny"
    puts "What a wonderful day! Just get out and take a nice walk to the park!"
when "rainy"
    puts "Don't forget your umbrella!"
when "cloudy"
    puts "It is such a great day to enjoy you book with a cup of coffee."
when "snowy"
    puts "If you are not in the mood for walking in the snow, take a glass of wine and enjoy the view"
else
    puts "Be prepared and go happy!"
end